  admission_date: { type: Date },
  firstname: { type: String, required: true },
  middlename: { type: String },
  lastname: { type: String },
  image: { type: String },
  mobileno: { type: String },
  email: { type: String },
  state: { type: String },
  city: { type: String },
  pincode: { type: String },
  religion: { type: String },
  dob: { type: Date },
  current_address: { type: String },
  permanent_address: { type: String },
  category_id: { type: mongoose.Schema.Types.ObjectId, ref: 'Category' },
  category: { type: String },
  adhar_no: { type: String },
  samagra_id: { type: String },
  bank_account_no: { type: String },
  bank_name: { type: String },
  ifsc_code: { type: String },
  guardian_name: { type: String },
  guardian_relation: { type: String },
  guardian_email: { type: String },
  guardian_phone: { type: String },
  guardian_address: { type: String },
  is_active: { type: Boolean, default: true },
  created_at: { type: Date, default: Date.now },
  updated_at: { type: Date, default: Date.now },
  father_name: { type: String },
  rte: { type: String },
  gender: { type: String },
  app_key: { type: String },
  parent_app_key: { type: String },
  father_phone: { type: String },
  father_occupation: { type: String },
  mother_name: { type: String },
  mother_phone: { type: String },
