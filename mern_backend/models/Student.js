  measurement_date: { type: Date },
  blood_group: { type: String },
  school_house_id: { type: mongoose.Schema.Types.ObjectId, ref: 'SchoolHouse' },
  father_pic: { type: String },
  mother_pic: { type: String },
  guardian_pic: { type: String },
  dis_reason: { type: String },
  dis_note: { type: String },
  disable_at: { type: Date },
